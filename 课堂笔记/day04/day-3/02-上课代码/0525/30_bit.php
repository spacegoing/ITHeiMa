<?php

// 原码: 最高位为符号位,0表示正数,1表示负数,其他位表示值
// 反码: 正数的反码,原码,补码都是它本身
// 		负数的反码,除去符号位不变,其他位按位取反
// 补码: 正数的反码,原码,补码都是它本身
//		负数的补码, 负数的反码 + 1;


// 位运行在实际开发过程中一般都是 正数
// 5
//00000000 00000000 00000000 00000101
// 9
//00000000 00000000 00000000 00001001

// 按位 与 &
//00000000 00000000 00000000 00000101
//00000000 00000000 00000000 00001001
//00000000 00000000 00000000 00000001
echo 5&9;

// 按位或 |
//00000000 00000000 00000000 00000101
//00000000 00000000 00000000 00001001
//00000000 00000000 00000000 00001101
//									1+4+8 = 13
echo '<br>',5|9;

// 按位非 ~
//00000000 00000000 00000000 00000101
//11111111 11111111 11111111 11111010  补码  负数 补码 - 1 就是 反码
//11111111 11111111 11111111 11111001	反码 变成 原码 符号位不变其他位按位取反
//10000000 00000000 00000000 00000110	原码 -6

echo '<br>',~5;

// 左移 <<  <<1表示左移1位 <<2表示左移2位 <<n 表示左移n位
//00000000 00000000 00000000 00000101
//0000000 00000000 00000000 000001010
//000000 00000000 00000000 0000010100
echo '<br>',5<<1;
echo '<br>',5<<2;
 
// 右移 >> 右移 左边空 . 
// 正数 左边补0
// 负数 左边补1
//00000000 00000000 00000000 00000101
//000000000 00000000 00000000 0000010
echo '<br>',5>>1;
echo '<br>',5>>2;


// 是否促销			1
// 是否打折			2
// 是否是海外		4
// 是否是推荐		8

// 0000 0001
// 0000 0010
// 0000 0100
// 0000 1000

// 记录2个状态   3 = 2 + 1
echo '<hr>';
echo 1 | 8  | 2 ;


echo '<hr>';

// 负数
// 5
// -9
// 5
//00000000 00000000 00000000 00000101
// -9
//10000000 00000000 00000000 00001001 原码
//11111111 11111111 11111111 11110110 反码
//11111111 11111111 11111111 11110111 补码

// 5-9  计算机中没有减法,只有加法
// 5 + (-9)
//00000000 00000000 00000000 00000101	
//11111111 11111111 11111111 11110111 +
//11111111 11111111 11111111 11111100 补码
//11111111 11111111 11111111 11111011 反码
//10000000 00000000 00000000 00000100 原码  -4


// -9
//11111111 11111111 11111111 11110111 补码
//111111111 11111111 11111111 1111011 补码
//111111111 11111111 11111111 1111010 反码
//100000000 00000000 00000000 0000101 原码  -5
echo -9>>1;

?>