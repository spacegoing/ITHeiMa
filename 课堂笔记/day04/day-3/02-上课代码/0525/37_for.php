<?php
$i=0;
$sum = 0;
for(;$i<10;) {

	echo '大家帅,才是真的帅';
	echo '<br>';
	$sum += $i; // ==> $sum = $sum + $i;

	$i++;
}

echo $sum;


echo '<hr>';

// 外循环用于控制 行
for($x=1;$x<=9;$x++) {

	// 内循环用于控制 列
	for($y=1;$y<=$x;$y++) {
		// 点(.)用于字符串的拼接
		// 逗号 用于 变量的分割
		// {} 主要用于 告诉变量的边界
		echo "{$x}x{$y} = " . $x*$y ,"&nbsp";
	}

	echo '<br>';
}

?>